class Command(BaseCommand):
    help = "Load ECO openings into the database"

    def handle(self, *args, **options):
        count = 0
        for eco_code, name, moves in OPENINGS:
            _, created = Opening.objects.update_or_create(
                eco=eco_code,
                defaults={"name": name, "moves": moves},
            )
            if created:
                count += 1

        self.stdout.write(self.style.SUCCESS(f"Loaded {len(OPENINGS)} openings ({count} new)"))
